CREATE DATABASE PrecioLuz;
USE PrecioLuz;

CREATE TABLE dias (
  id INT AUTO_INCREMENT PRIMARY KEY,
  fecha DATE NOT NULL UNIQUE,
  precio DECIMAL(6,3) NOT NULL
);

CREATE TABLE precios_horas (
  id INT AUTO_INCREMENT PRIMARY KEY,
  id_dia INT NOT NULL,
  hora TIME NOT NULL,
  precio DECIMAL(6,3) NOT NULL,
  FOREIGN KEY (id_dia) REFERENCES dias(id)
);

